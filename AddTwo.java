/*
 * Adds two given integers and prints the result in a fancy way.
 */
public class AddTwo {
	public static void main(String[] args) {
	        int a = 3;
		int b = 5;
		int c = a + b;
		
		int d = 9281;
		int e = 719;
		int f = d + e;
	System.out.println( a + " + " + b + " = " + c );
	System.out.println( d + " + " + e + " = " + f );
		
	
	}
}
